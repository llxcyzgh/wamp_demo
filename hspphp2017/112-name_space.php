/*
 命名空间用来解决这样的问题：
当项目越来越大，PHP文件会越来越多，再加上多人协同开发，在定义类、函数或者常量时，就不可避免的出现命名冲突，如何解决呢？

使用命名空间解决。
例如：班里有3个张三，来自不同的地方，如果直接喊张三时，这3个人就会很迷惑，为了避免这种情况，再叫张三时，加上区域名称：东北的张三，河北的张三，湖北的张三；其中，张三前面的区域修饰就是命名空间

所以我们在定义类、函数、常量时，在前面加上所属的空间

 */